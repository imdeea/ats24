					<h2>How are Conveyor Belts Made?</h2>
					<p>Standard conveyor belts are made out of mild steel framework with a powder finish. This makes them very suited to warehouses and factory areas that deal with food and drink. Both ends of the conveyor are fitted with terminal rollers, as this helps to reduce any friction when the belt is running on the underside. The conveyer is then assembled into the frame, which runs on the smooth bed, or rollers if the weight is considerable. The two ends are then joined, making a continuous cycle. Belts stretch over time, and for this reason, it is important to have a tensioning device fitted, so that the belt doesn&rsquo;t slip.</p>
					<p>To pull the belt, one end of the roller is fitted with a drive or motor. This can be positioned at one end of the conveyor, or it can be fitted part-way if the belt is going to be reversible.</p>

